#include <stdio.h>
#include <stdlib.h>
/**
* op_add - addition operator
* @a: first int
* @b: second int
* Return: sum of 2 numbers
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - addition operator
 * @a: first int
 * @b: second int
 * Return: subtract of 2 numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - addition operator
 * @a: first int
 * @b: second int
 * Return: multiply of 2 numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - addition operator
 * @a: first int
 * @b: second int
 * Return: divide of 2 numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - addition operator
 * @a: first int
 * @b: second int
 * Return: modulo of 2 numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
