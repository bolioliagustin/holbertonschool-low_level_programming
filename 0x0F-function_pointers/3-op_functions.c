#include <stdio.h>
/**
* op_add - addition operator
* @a: first int
* @b: second int
* Return: sum of 2 numbers
*/
int op_add(int a, int b)
{
	return(a + b);
}

/**
 * op_add - addition operator
 * @a: first int
 * @b: second int
 * Return: subtract of 2 numbers
 */
int op_sub(int a, int b)
{
	return(a - b);
}
/**
 * op_add - addition operator
 * @a: first int
 * @b: second int
 * Return: multiply of 2 numbers
 */
int op_mul(int a, int b)
{
	return(a * b);
}
